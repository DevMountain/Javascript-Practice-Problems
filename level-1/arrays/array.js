// Return the first item in a array

function first(arr) {
    return arr[0];

}


// Return the length of the array

function length(arr) {

    var lengthCount = arr.length;

    return lengthCount
}



// return the last object in the array

function last(arr) {

    var lastItem = arr[arr.length -1]

    return lastItem
}


// Add number to the array

function addItem(array, number) {
    array.push(number)
    return array
}


